import time

from core.global_workspace import GlobalWorkspace


class Executor:

    def __init__(self, workspace):

        self.workspace = workspace


    def execute(self, plan):

        completed = []

        total = len(plan)

        for step, task in enumerate(plan, start=1):

            print(f"\n[Executor] Step {step}/{total}")

            print(task)

            self.workspace.broadcast(

                "Executor",

                task,

                category="execution",

                priority=0.85,

                metadata={

                    "step": step,

                    "total_steps": total,

                    "status": "running"

                }

            )

            #
            # Future integrations go here
            #
            # Ask Groq
            # Search Memory
            # Query Workspace
            # Execute Tool
            #

            time.sleep(1)

            self.workspace.broadcast(

                "Executor",

                f"Completed: {task}",

                category="execution_complete",

                priority=0.85,

                metadata={

                    "step": step,

                    "status": "complete"

                }

            )

            completed.append(task)

        return completed


if __name__ == "__main__":

    workspace = GlobalWorkspace()

    plan = [

        "Retrieve contradiction",

        "Collect workspace events",

        "Ask Groq for analysis",

        "Generate summary",

        "Store result"

    ]

    executor = Executor(workspace)

    completed = executor.execute(plan)

    print("\n====================")

    print("Execution Finished")

    print("====================")

    for task in completed:

        print("✓", task)
